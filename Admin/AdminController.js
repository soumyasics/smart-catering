const customers = require("../Customer/CustomerSchema");
const complaintSchema = require("../Customer/complaintSchema");
const staffs = require("../Staff/StaffSchema");

//View all staff

const viewAllCatering = (req, res) => {
  staffs
    .find()
    .exec()
    .then((data) => {
      if (data.length > 0) {
        res.json({
          status: 200,
          msg: "Data obtained successfully",
          data: data,
        });
      } else {
        res.json({
          status: 200,
          msg: "No Data obtained ",
        });
      }
    })
    .catch((err) => {
      res.json({
        status: 500,
        msg: "Data not Inserted",
        Error: err,
      });
    });
};

